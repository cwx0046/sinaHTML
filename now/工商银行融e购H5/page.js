/**
 * 生成小题库
 */
function setNewData(size) {
	data = {};
	data.desc = "答题数据库",
	data.items = [];
	
	var indexUsed = [];
	while (data.items.length < size) {
		//不断随机以填满数据
		var org_dataQuesGroupNum = org_data.items.length / 2;
		var randomQuestionIndex = parseInt(Math.random() * org_dataQuesGroupNum);
		var isUsed = false;
		for (var i = 0; i < indexUsed.length; i++) {
			if (randomQuestionIndex == indexUsed[i]) {
				isUsed = true;
				break;
			}
		}
		if (isUsed) {
			continue;
		} else {
			//存入答题库吧
			data.items.push(clone(org_data.items[randomQuestionIndex]));
		}
	}
}

/** clone obj */
function clone(obj){  
    function Clone(){}  
    Clone.prototype = obj;  
    var o = new Clone();  
    for(var a in o){  
        if(typeof o[a] == "object") {  
            o[a] = clone(o[a]);  
        }  
    }  
    return o;  
}  

/**
 * 绘制题目到页面
 */
function drawNewData() {
	var html = template('targetTemplate1', data);
	document.getElementById('target1').innerHTML = html;
	var html = template('targetTemplate2', data);
	document.getElementById('target2').innerHTML = html;
}


